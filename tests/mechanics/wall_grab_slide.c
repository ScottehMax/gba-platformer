static int jumpIntoWallFrame = -1;
static int climbGrabFrame = -1;
static int lostGrabFrame = -1;
static float startY = -1.0f;
static float maxSlide = 0.0f;
static float minY = 999.0f;

static void verifyWallGrab(const Player* player, int frame, TestResults* results) {
    // Detect jump into wall (around frame 290-295)
    if (jumpIntoWallFrame < 0 && frame >= 290 && frame <= 295 && (wall_grab_inputs[frame] & KEY_A)) {
        jumpIntoWallFrame = frame;
        printf("  INFO: Jump into wall at frame %d\n", frame);
    }

    // Detect when player tries to grab wall after jump (around frame 308+)
    if (climbGrabFrame < 0 && frame >= 308 && player->stateMachine.state == ST_CLIMB) {
        climbGrabFrame = frame;
        startY = player->y / 256.0f;
        printf("  INFO: Grabbed wall at frame %d, y=%.1fpx\n", frame, startY);
        printf("  INFO: Initial state: %d, vy: %.1f\n", player->stateMachine.state, player->vy);
    }

    // Monitor for losing wall grab and track full extent of sliding
    if (climbGrabFrame >= 0 && frame > climbGrabFrame + 10) {
        float currentY = player->y / 256.0f;
        float slideAmount = currentY - startY;

        // Track min Y (highest point) and max slide
        if (currentY < minY) minY = currentY;
        if (slideAmount > maxSlide) maxSlide = slideAmount;

        // Print status every 10 frames for visibility
        if ((frame - climbGrabFrame) % 10 == 0 && frame <= 400) {
            printf("  INFO: Frame %d (+%d): y=%.1fpx (Δ%.1f), state=%d, vy=%.1f\n",
                   frame, frame - climbGrabFrame, currentY, slideAmount,
                   player->stateMachine.state, player->vy);
        }

        // Detect when we lose the grab (first time entering NORMAL state)
        if (lostGrabFrame < 0 && player->stateMachine.state == ST_NORMAL) {
            lostGrabFrame = frame;
            printf("  INFO: Lost wall grab at frame %d (%.1fs after grabbing)\n",
                   frame, (frame - climbGrabFrame) / 60.0f);
            printf("        Position at loss: y=%.1fpx (grabbed at %.1fpx)\n",
                   currentY, startY);
        }

        // After losing grab, track how far we slide down
        if (lostGrabFrame >= 0 && frame == 450) {
            // Final check at end of monitoring period
            printf("  INFO: Final position at frame %d: y=%.1fpx\n", frame, currentY);
            printf("  INFO: Total slide from grab point: %.1fpx down\n", maxSlide);
            printf("  INFO: Highest point reached: y=%.1fpx (%.1fpx above grab)\n",
                   minY, startY - minY);

            // The bug: player slides WAY down (more than 20 pixels) after losing grip
            if (maxSlide > 20.0f) {
                printf("  FAIL: Player slid %.1fpx down from grab point!\n", maxSlide);
                printf("        This indicates wall grab was lost and player fell to ground\n");
                printf("        With wall snapping, player should maintain grip or slide minimally\n");
                results->failed++;
            }
        }
    }
}

